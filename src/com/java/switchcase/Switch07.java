package com.java.switchcase;

public class Switch07 {

	public static void main(String[] args) {

		int numero = 50;

		switch (numero) {
		case 1:
			System.out.println("One");
			break;
		case 3:
			System.out.println("Three");
			break;
		case 5:
			System.out.println("Five");
			break;

		default:
			System.out.println("Unknown");
			break;
		}

	}

}
